/* ===== Utility Functions ===== */
window.GTA = window.GTA || {};

GTA.Utils = (function () {
  /**
   * Format number as GTA dollar currency string
   */
  function formatCurrency(num) {
    if (num == null || isNaN(num)) return '$0';
    if (num >= 1000000) {
      return '$' + (num / 1000000).toFixed(2) + 'M';
    }
    if (num >= 1000) {
      return '$' + (num / 1000).toFixed(0) + 'K';
    }
    return '$' + num.toLocaleString('en-US');
  }

  /**
   * Format timestamp to date string
   */
  function formatDate(timestamp) {
    if (!timestamp) return '';
    const d = new Date(timestamp);
    const yyyy = d.getFullYear();
    const mm = String(d.getMonth() + 1).padStart(2, '0');
    const dd = String(d.getDate()).padStart(2, '0');
    return yyyy + '/' + mm + '/' + dd;
  }

  /**
   * Format timestamp to Chinese date string
   */
  function formatDateCN(timestamp) {
    if (!timestamp) return '';
    const d = new Date(timestamp);
    return d.getFullYear() + '年' + (d.getMonth() + 1) + '月' + d.getDate() + '日';
  }

  /**
   * Debounce function
   */
  function debounce(fn, ms) {
    let timer;
    return function (...args) {
      clearTimeout(timer);
      timer = setTimeout(() => fn.apply(this, args), ms);
    };
  }

  /**
   * Clamp value between min and max
   */
  function clamp(val, min, max) {
    return Math.max(min, Math.min(max, val));
  }

  /**
   * Generate a simple unique ID
   */
  function generateId() {
    return 'id_' + Date.now().toString(36) + '_' + Math.random().toString(36).substring(2, 8);
  }

  /**
   * Detect if current month
   */
  function isCurrentMonth(timestamp) {
    const d = new Date(timestamp);
    const now = new Date();
    return d.getMonth() === now.getMonth() && d.getFullYear() === now.getFullYear();
  }

  /**
   * Escape HTML characters
   */
  function escapeHtml(str) {
    const div = document.createElement('div');
    div.appendChild(document.createTextNode(str));
    return div.innerHTML;
  }

  return {
    formatCurrency,
    formatDate,
    formatDateCN,
    debounce,
    clamp,
    generateId,
    isCurrentMonth,
    escapeHtml
  };
})();

